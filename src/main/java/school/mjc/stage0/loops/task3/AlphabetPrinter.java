package school.mjc.stage0.loops.task3;

public class AlphabetPrinter {
    public static void printAlphabet() {

        char ch;
        for (ch = 'a'; ch <= 'z'; ch++){
            System.out.println(ch);
        }
    }

    public static void main(String[] args) {
        printAlphabet();
    }
}
